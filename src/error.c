#include "cub3d.h"

void	ft_error(char *msg)
{
	get_core()->error = TRUE;
	ft_putstr_fd(msg, 2);
	if (get_map()->map_str)
		ft_free_matrix(get_map()->map_str);
	exit(EXIT_FAILURE);
}
